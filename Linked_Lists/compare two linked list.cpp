    while(headA != NULL && headB != NULL)
    {
        if(headA->data!=headB->data)
        {
            flag=1;
            break;
        }
        headA = headA->next;
        headB = headB->next;
    }
    
    if(flag==1 || headA!=NULL || headB!=NULL)
        return 0;
